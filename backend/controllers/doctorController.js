/**
 * @desc    Get AI-generated clinical summary for a patient
 * @route   GET /api/doctor/patient/:id/summary
 * @access  Private/Doctor+Staff
 */
exports.getPatientClinicalSummary = async (req, res) => {
    try {
        const patientId = req.params.id;
        const patient = await Patient.findById(patientId);

        if (!patient) {
            return res.status(404).json({ message: 'Patient profile not found' });
        }

        const { memberId } = req.query;
        let memberFilter = {};
        if (memberId && memberId !== 'null') {
            memberFilter = { 'bookedForMember.memberId': memberId };
        } else if (memberId === 'null') {
            memberFilter = { 'bookedForMember.memberId': null };
        }

        const baseQuery = { patientId: patient._id, ...memberFilter };

        // Get past prescriptions (last 5)
        const prescriptions = await Prescription.find(baseQuery)
            .sort({ createdAt: -1 })
            .limit(5);

        // Get past lab tests (last 5 completed OR those with PDFs)
        const labTests = await LabTest.find({
            ...baseQuery,
            $or: [{ status: 'completed' }, { reportPDF: { $ne: null } }]
        })
            .sort({ updatedAt: -1 })
            .limit(10); // Check more to find PDFs

        // Get PDF Report Findings (latest 2 reports)
        const pdfReports = labTests.filter(lt => lt.reportPDF).slice(0, 2);
        let reportFindings = '';
        
        for (const report of pdfReports) {
            // Normalize path (handle both absolute and relative)
            let absolutePath = path.isAbsolute(report.reportPDF) 
                ? report.reportPDF 
                : path.resolve(process.cwd(), report.reportPDF);
            
            // Fallback if not found: try relative to __dirname (backward compatibility)
            if (!fs.existsSync(absolutePath)) {
                absolutePath = path.join(__dirname, '..', report.reportPDF);
            }

            console.log(`[Clinical Summary] Attempting to extract text from: ${absolutePath}`);
            const text = await extractTextFromPDF(absolutePath);
            if (text) {
                console.log(`[Clinical Summary] Successfully extracted ${text.length} chars from ${report.testName}`);
                reportFindings += `--- Report: ${report.testName} ---\n${text}\n\n`;
            } else {
                console.warn(`[Clinical Summary] Extraction returned empty for: ${report.testName}`);
            }
        }

        // Defensive mapping to ensure no crashes on missing fields
        const summaryParams = {
            patient,
            chronicDiseases: (patient.chronicDiseases || []).map(d => d.disease || 'Unknown'),
            allergies: (patient.allergies || []).map(a => a.allergen || 'Unknown'),
            prescriptions: (prescriptions || []).map(p => ({
                date: p.createdAt ? new Date(p.createdAt).toLocaleDateString() : 'Unknown',
                diagnosis: p.diagnosis || 'Not specified',
                medicines: (p.medicines || []).map(m => m.name).join(', ') || 'None'
            })),
            labResults: (labTests || []).map(lt => ({
                testName: lt.testName || 'Unknown',
                result: (lt.results || []).map(r => `${r.parameter}: ${r.value}${r.unit || ''}`).join(', ') || 'See report'
            })),
            reportFindings // Pass extracted PDF text
        };

        const summaryData = await geminiService.getPatientProfileSummary(summaryParams);
        res.json(summaryData);
    } catch (error) {
        console.error('getPatientClinicalSummary error details:', error);
        res.status(500).json({ 
            success: false, 
            message: 'Error generating patient summary',
            details: error.message 
        });
    }
};
